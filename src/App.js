function Movie({ movie, onSelectMovie }) {
  return (
    <li onClick={() => onSelectMovie(movie.imdbID)}>
      <img src={movie.Poster} alt={`${movie.Title} poster`} />
      <h3>{movie.Title}</h3>
      <div>
        <p>
          <span>üóì</span>
          <span>{movie.Year}</span>
        </p>
      </div>
    </li>
  );
}
function SelectedMovie({ selectedId, onCloseMovie, onAddWatched, watched }) {
  const isWatched = watched.map((movie) => movie.imdbID).includes(selectedId);
  const [movie, setMovie] = useState({});
  const [userRating, setUserRating] = useState(
    isWatched ? watched.filter((movie) => movie.imdbID === selectedId).at(0) : 0
  );
  const {
    Title: title,
    Year: year,
    Poster: poster,
    Runtime: runtime,
    imdbRating,
    Plot: plot,
    Released: realeased,
    Actors: actors,
    Director: director,

    Genre: genre,
  } = movie;
  const [isLoading, setIsLoading] = useState(false);

  useEffect(
    function () {
      const cb = function (e) {
        if (e.code === "Escape") {
          console.log("hey");

          onCloseMovie();
        }
      };
      document.addEventListener("keydown", cb);
      return function () {
        document.removeEventListener("keydown", cb);
      };
    },
    [onCloseMovie]
  );
  useEffect(
    function () {
      async function getMovieDetails() {
        setIsLoading(true);
        const res = await fetch(
          `https://www.omdbapi.com/?apikey=${API_KEY}&i=${selectedId}`
        );
        if (!res.ok) {
          throw new Error("Something went Wrong with fetching movie data");
        }
        const data = await res.json();
        if (data.Response === "False") throw new Error("Movie not found");
        setIsLoading(false);
        setMovie(data);
      }
      getMovieDetails();
    },
    [selectedId]
  );
  function handleAdd() {
    const newMovie = {
      imdbID: selectedId,
      title,
      year,
      Poster: poster,
      runtime: Math.round(Number(runtime.split(" ")[0])),
      imdbRating: Math.round(Number(imdbRating)),
      plot,
      realeased,
      actors,
      director,
      genre,
      userRating,
    };
    onAddWatched(newMovie);
    onCloseMovie();
  }
  useEffect(
    function () {
      if (!title) return;
      document.title = `Movie | ${title}`;
      return function () {
        document.title = "UsePopCorn";
      };
    },
    [title]
  );
  return (
    <div className="details">
      {isLoading ? (
        <Loader />
      ) : (
        <>
          <header>
            <button className="btn-back" onClick={onCloseMovie}>
              x
            </button>

            <img src={poster} alt={`Poster for ${title}`} />
            <div className="details-overview">
              <h2>{title}</h2>
              <p>
                {realeased} &bull; {runtime}
              </p>
              <p>{genre}</p>
              <p>
                <span>üåü</span> {imdbRating}
              </p>
            </div>
          </header>

          <section>
            <div className="rating">
              {!isWatched ? (
                <>
                  <StarRating
                    defaultRating={userRating}
                    maxRating={10}
                    size={24}
                    onSetRating={setUserRating}
                  />
                  <button className="btn-add" onClick={handleAdd}>
                    Watched ‚úÖ
                  </button>
                </>
              ) : (
                <p>You already watched this movie </p>
              )}
            </div>

            <p>
              <em>{plot}</em>
            </p>
            <p>Starring {actors}</p>
            <p>Directed by {director}</p>
          </section>
        </>
      )}
    </div>
  );
}
